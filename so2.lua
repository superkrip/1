function printtypenumber(base)
    if base > 0 then print("Duong")
    else
        print("Am")
    end
end

function main()
    local a = 10
    local b = -324
    local c = -242
    local d = 3324
    local e = 32
    printtypenumber(a)
    printtypenumber(b)
    printtypenumber(c)
    printtypenumber(d)
    printtypenumber(e)
end

main()